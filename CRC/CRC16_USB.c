//=============================================================================
// Compute the CRC16-USB from the byte stream in parameter (with reverse table)
//=============================================================================
uint16_t ComputeCRC16USB(const uint8_t* data, uint32_t size)
{
	if (data == NULL) return 0;
	uint16_t crc = 0xFFFF; // Start value

	while (size-- != 0)
	{
		crc = (crc >> 8) ^ CRC16USB_TableRev[(crc ^ *data++) & 0xFF];
	}

	crc ^= 0xFFFF; // Final XOR
	return crc;
}


